namespace HomeworkHelpers.Enumerations
{
    /// <summary>
    /// Enumeration for different file length measurements
    /// </summary>
    public enum FileLength
    {
        /// <summary>
        /// Kilobyte length
        /// </summary>
        Kbyte = 1024,

        /// <summary>
        /// Megabyte length
        /// </summary>
        Mbyte = 1024 * Kbyte
    }
}
